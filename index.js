function createPrintout(atribute, atributeName) {
    return "<tr>" +
            "<th><b>" + atributeName + "</b></th>" + 
            "<th>" + atribute.min.toFixed(2) + "</th>" +
            "<th>" + atribute.max.toFixed(2) + "</th>" +
            "<th>" + atribute.mean.toFixed(2) + "</th>" +
           "</tr>"
}

