class Hex {

    constructor(center_lng, center_lat, size, distortion) 
    {
        this.center_lng = center_lng;
        this.center_lat = center_lat;
        let height = Math.sqrt(3) * size/2;
        this.triangleCoords = [
            { lng: this.center_lng - 1/2*size*distortion, lat: this.center_lat - height },
            { lng: this.center_lng + 1/2*size*distortion, lat: this.center_lat - height },
            { lng: this.center_lng + size*distortion, lat: this.center_lat},
            { lng: this.center_lng + 1/2*size*distortion, lat: this.center_lat + height },
            { lng: this.center_lng - 1/2*size*distortion, lat: this.center_lat + height },
            { lng: this.center_lng - size*distortion, lat: this.center_lat}
        ];
    }

    draw() {
        const bermudaTriangle = new google.maps.Polygon({
            paths: this.triangleCoords,
            strokeColor: "black",
            strokeOpacity: 0.8,
            strokeWeight: 3,
            fillColor: "grey",
            fillOpacity: 0.35,
        });
        return bermudaTriangle;
    }

}

