function showArrays(event) {
    const polygon = this;

    let contentString =
        "Coordinates: " +
        event.latLng.lat() +
        ", &nbsp" +
        event.latLng.lng() +
        "<br>";

    contentString += '<br><img '; 
    switch(this.get("fillColor")) {
        case 'red':
        case 'black':
            contentString += 'src="puffin-lookin-down.png"';
            break;
        case 'lime':
        case 'olive':
            contentString += 'src="happy-puffin.png"';
            break;
        default:
            contentString += 'src="idle-puffin.png"';
            break;
    }
    contentString += ' height="120" class="center">';
    hexParams = this.get("hex");

    contentString += "<table>" +
                    "<tr>" +
                        "<th>Param</th>" +
                        "<th>Min</th>" +
                        "<th>Max</th>" +
                        "<th>Avg</th>" +
                    "</tr>"

    contentString += createPrintout(hexParams.temperature, "Temperature (K)");
    contentString += createPrintout(hexParams.wind_speed, "Wind Speed (m/s)");
    contentString += createPrintout(hexParams.vegetation, "Vegetation (m^2 forest/m^2)");
    contentString += createPrintout(hexParams.air_density, "Air Density (kg/m^3)");
    contentString += "</table>";

    infoWindow.setContent(contentString);
    infoWindow.setPosition(event.latLng);
    infoWindow.open(map);
}
